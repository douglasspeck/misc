                <h3>É possível expressar conceitos abstratos na língua de sinais?</h3>
                <blockquote class="high-quote">sinais não são gestos</blockquote>
                <p>Gestos são, para as línguas orais, <strong><dfn>elementos paralinguísticos</dfn></strong>, isto é, complementos linguísticos de um elemento "principal". Os sinais, por outro lado, são elementos completos por si. É importante fazer essa diferenciação.</p>
