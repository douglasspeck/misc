            <section>
                <h3>O surdo tem dificuldade de escrever porque não sabe falar a língua oral?</h3>
                <p>A escrita é uma habilidade cognitiva que demanda esforço de todos. Entretanto, para os surdos, há uma barreira maior: a escrita possui uma relação fônica direta com a língua oral, que não é, para o surdo, familiar. Assim, para o surdo, a escrita é como um conjunto de símbolos abstratos.</p>
                <p>O preconceito enfrentado na aprendizagem provoca nos surdos brasileiros em processo de alfabetização um sentimento de aversão à língua, além de impotência e baixa autoestima.</p>
            </section>
            <section>
                <h3>O uso da língua de sinais atrapalha a aprendizagem da língua oral?</h3>
                <p>Novamente é possível associar com outros idiomas. As pessoas não temem a educação bilíngue de crianças oralizadas com pais de diferentes nacionalidades. Por que então há uma rejeição tão grande ao ensino bilíngue de crianças surdas?</p>
                <p>O ensino da escrita para os surdos deve ser promovido na língua de sinais, uma vez que o consenso entre especialistas aponta para o fracasso do ensino da linguagem escrita quando a língua de sinais não é usada para instruir o aluno.</p>
            </section>
