            <h2>Capítulo 3 – A surdez</h2>
            <section>
                <h3>A surdez é um problema para o surdo?</h3>
                <p>Ver a surdez como um problema evidencia uma concepção patológica da surdez, que inferioriza o surdo. Adorei conhecer, ao longo do livro, a perspectiva da surdez como uma diferença linguística e cultural.</p>
                <p>Com essa perspectiva em mente, é evidente que a surdez trata-se de um objeto de estudo maior da educação e da linguística que da medicina e fonoaudiologia, essencialmente.</p>
                <p>A visão da surdez como problema está associada a uma necessidade de autoafirmação e uma dificuldade de lidar com o diferente e/ou desconhecido.</p>
            </section>
            <section>
                <h3>A surdez é uma deficiência?</h3>
                <p>A prerrogativa de que a surdez é uma deficiência abre espaço para a visão patológica e de inferiorização do indivíduo surdo.</p>
                <p>Achei muito interessantes os relatos onde há a "inversão de valores", especialmente no que se refere a crianças surdas que crescerem em um meio surdo.</p>
            </section>
            <section>
                <h3>Por que a surdez é vista negativamente pela sociedade?</h3>
                <p>O discurso médico é supervalorizado de maneira geral, enquanto o discurso linguístico (mesmo se considerarmos apenas dentro do meio oral) é desvalorizado. Esse contraste reforça muito mais o estigma de que a surdez é uma anormalidade, uma infelicidade, ao invés de uma diferença.</p>
                <blockquote>
                    <p>todo o processo de normalização é homogeneizador, ou seja, visa trazer cada elemento desviante para o espaço igualitário da norma.</p>
                    <cite>Souza, 2002:138</cite>
                </blockquote>
            </section>
            <section>
                <h3>A surdez é hereditária?</h3>
                <ul>
                    <li>Respondendo à pergunta, sim. Há condições genéticas relacionadas à surdez.</li>
                    <li>Reitero que Graham Bell era um grandissíssimo babaca.</li>
                </ul>
            </section>
            <section>
                <h3>Há diferentes tipos e graus de surdez?</h3>
                <p>Há cerca de 70 tipos (causas) de surdez hereditária. A surdez pode ser dividida em três categorias, de acordo com a alteração que a provoca. Além disso, ela é quantificada através de graus.</p>
                <section>
                    <h4>Tipos de surdez:</h4>
                    <ul>
                        <li>condutiva: alteração no ouvido externo ou médio</li>
                        <li>neurossensorial: alteração na cóclea e/ou nervo auditivo</li>
                        <li>mista: engloba ambas as alterações</li>
                    </ul>
                </section>
                <section>
                    <h4>Graus de surdez:</h4>
                    <p>Os graus de surdez são apontados de acordo com os limiares da audição, em decibéis. O grau de surdez pode intensificar, saindo mesmo de uma surdez leve até a profunda.</p>
                    <ul>
                        <li>normal: até 25 dB</li>
                        <li>leve: de 26 a 40 dB</li>
                        <li>moderada: de 41 a 55 dB</li>
                        <li>moderadamente severa: de 56 a 70 dB</li>
                        <li>severa: de 71 a 90 dB</li>
                        <li>profunda: acima de 91 dB</li>
                    </ul>
                    <p>É importantíssimo destacar que <strong>o grau de surdez não determina a relação de cada indivíduo com a língua de sinais</strong>.</p>
                </section>
            </section>
            <section>
                <h3>Aparelhos auditivos ajudam o surdo a ouvir melhor?</h3>
                <p>De maneira geral, os aparelhos auditivos servem para ajudar na percepção de ruídos. É mais útil para aqueles indivíduos com maior resíduo auditivo e dificilmente auxilia surdos com perda auditiva profunda a compreender a língua oral.</p>
                <p>Há de se fazer uma distinção entre surdos <strong>pós-linguais</strong>, que tiveram contato com a língua sonora por certo tempo, e <strong>pré linguais</strong>, que não tiveram contato com a língua sonora.</p>
            </section>
            <section>
                <h3>O implante coclear recupera a audição do surdo?</h3>
                <p>O implante coclear é um procedimento complexo e invasivo que depende de inúmeros fatores para funcionar. Além disso, os relatos de surdos que passaram pelo mesmo apontam que os resultados não são nenhum bem-bom.</p>
                <p>Ademais, os implante cocleares estão muito pautados na concepção assimiladora e normalizadora, sendo vistos como uma "cura". O que é incabível, visto que a surdez não é um problema a ser resolvido, e sim uma diferença.</p>
            </section>
            <section>
                <h3>A surdez compromete o desenvolvimento cognitivo-linguístico do surdo?</h3>
                <blockquote>
                    <p>Não é a <strong>surdez</strong> que compromete o desenvolvimento do surdo, e sim a falta de acesso a uma língua.</p>
                </blockquote>
                <p>Exigir do surdo o desenvolvimento pleno de suas capacidades em um meio onde sequer é capaz de comunicar-se casualmente é como o adágio do peixe que é mandado a subir em uma árvore.</p>
                <section>
                    <h4>Processo de Aquisição de Linguagem</h4>
                    <p>Esse processo é observado tanto em crianças ouvintesquanto em crianças surdas.</p>
                    <ul class="flowchart">
                        <li>Período pré-linguístico</li>
                        <li>Estágio de uma palavra</li>
                        <li>Primeiras combinações lexicais</li>
