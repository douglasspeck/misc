            <section>
                <h3>Que momento nós vivemos?</h3>
                <p>Estamos em uma crescente de vitórias para a comunidade surda e para a Libras. Como apontado pela autora, é um momento <q>do surdo e para o surdo</q>.</p>
                <p>Há de se atentar, todavia, aos discursos mascarados em prol de interesses distintos. Ademais, não basta apenas que as determinações estejam escritas. A inclusão social é um processo que permeia todas as esferas da sociedade, na teoria e, principalmente, na prática. Exemplo disso é que o inglês é parte obrigatória do currículo escolar, mas apenas uma parcela ínfima da população é efetivamente capaz de se comunicar nessa língua.</p>
