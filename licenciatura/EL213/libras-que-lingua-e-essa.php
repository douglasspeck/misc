                <h3>O surdo precisa da língua portuguesa para sobreviver na sociedade majoritária ouvinte?</h3>
                <p>A defesa dessa ideia é, essencialmente, monolinguista, uma vez que não abre espaço para variação linguística.</p>
                <p>Para começo de conversa, nem todo surdo é oralizado, o que já evidencia a independência dos mesmos em relação à língua portuguesa. Mesmo aqueles que usam a língua portuguesa não o fazem em uma relação de dependência.</p>
            </section>
