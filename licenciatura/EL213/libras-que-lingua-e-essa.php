                <h3>É uma língua exclusivamente icônica?</h3>
                <ul>
                    <li>Às vezes há sinais icônicos, mas assim também há nas línguas orais (onomatopeias, por exemplo)</li>
                    <li>A língua é <q>um fenômeno convencional mantido por um 'acordo coletivo tácito' entre os falantes de uma determinada comunidade</q></li>
                    <li>Quando pensamos nas sentenças através da "metáfora do pacote", é importante lembrar que as línguas são "empacotadas" de jeitos diferentes. Então <strong>não há língua mais compacta ou mais complexa</strong>, apenas línguas com formas diferentes de construção.
                        <ul>
                            <li>Isso deve ser pensado especialmente na tradução de Libras para o português e vice-versa. A Libras não é mais "rudimentar" por não precisar, por exemplo, de conectivos ou flexões verbais.</li>
                        </ul>
                    </li>
                </ul>
