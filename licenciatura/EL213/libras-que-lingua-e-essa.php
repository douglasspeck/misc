                <h3>Direitos linguísticos dos surdos</h3>
                <p>Esta é uma lista de direitos propostos por Gomes de Matos, em 1984, na Revista de Cultura Vozes.</p>
                <ol>
                    <li><strong>Direito à igualdade linguística.</strong> O surdo tem direito a ser tratado linguisticamente com respeito e em condições de igualdade.</li>
                    <li><strong>Direito à aquisição da linguagem.</strong> O surdo tem direito a adquirir sua língua materna, a língua de sinais, mesma que essa não seja a língua de sinais.</li>
                    <li><strong>Direito de aprendizagem da língua materna.</strong> Todo surdo tem direito a ser alfabetizado em tempo hábil e de se desenvolver linguisticamente, segundo preconizado pela educação permanente.</li>
                    <li><strong>Direito ao uso da língua materna.</strong> O surdo tem direito de usar sua língua materna em caráter permanente.</li>
                    <li><strong>Direito a fazer opções linguísticas.</strong> O surdo tem o direito de optarpor uma língua oral ou de sinais segundo suas necessidades comunicativas.</li>
                    <li><strong>Direito à preservação e à defesa da língua materna.</strong> Como minoria linguística, os surdos têm o direito de preservar e defender o uso da língua materna.</li>
                    <li><strong>Direito ao enriquecimento e à valorização da língua materna.</strong> Todo surdo tem direito de contribuir com o acervo lexical da língua materna e de valorizá-la como instrumento de comunicação nos planos local (municipal, estadual, regional, nacional) e internacional.</li>
                    <li><strong>Direito à aquisição/aprendizagem de uma segunda língua.</strong> Todo surdo, após sua escolarização inicial em língua de sinais, tem o direito de aprender uma ou mais línguas (além da materna).</li>
                    <li><strong>Direito à compreensão e à produção plenas.</strong> O surdo tem direito de usar a língua que mais lhe convier, oral ou de sinais, no intuito de compreender seu interlocutor e de se fazer entender por ele. No caso do uso da língua oral, o surdo tem direito de cometer lapsos, de se autocorrigir, de empenhar-se a fim de ser claro, preciso e relevante. O mesmo deve valer para a língua de sinais.</li>
                    <li><strong>Direito de receber tratamento especializado para distúrbios da comunicação.</strong> Todo surdo tem o direito de reivindicar e de receber tratamento especializado para a aquisição de uma língua oral (se assim desejar).</li>
                    <li><strong>Direito linguístico da criança surda.</strong> Direito de ser "compreendida" pelos pais; direito de receber dos pais dados linguísticos necessários para seu desenvolvimento linguístico inicial (no período de aquisição da língua materna). No caso de os pais serem ouvintes, estes devem dar aos filhos surdos a possibilidade de mútua compreensão, aprendendo, tão logo descubram a surdez dos filhos, a língua de sinais.</li>
                    <li><strong>Direito linguístico dos pais de crianças surdas.</strong> Direito de aprender e usar sem opressão a língua de sinais, canal natural de comunicação para o filho surdo, para que possa comunicar-se com ela na vida diária e no período em que a interação pais e filhos se faz necessária para a criança.</li>
                    <li><strong>Direito linguístico do surdo aprendiz da língua oral.</strong> Direito de "errar" oralmente ou por escrito sem ser punido, humilhado, por opções linguísticas inadequadas; deireito de ser sensibilizado contra os preconceitos e discriminações de natureza linguística (ou sociolinguística).</li>
                    <li><strong>Direito do professor surdo e de surdos.</strong> Direito de receber formação sobre a natureza da língua de sinais, sua estrutura e seus usos e de ensinar nessa língua, meio mais natural de comunicação com e/ ou entre os surdos.</li>
                    <li><strong>Direito linguístico do surdo como indivíduo bilíngue.</strong> Direito de mudar de uma língua para outra de acordo com a situação que se lhe apresente, desde que assegure a compreensão da mensagem pelo ouvinte.</li>
                    <li><strong>Direito linguístico do surdo como conferencista.</strong> Direito de proferir palestras na língua de sinais, fazendo-se compreender e contando, para isso, com intérpretes ouvintes que dominem sua língua de sinais e a língua oral oficial da situação de um congresso, por exemplo.</li>
                    <li><strong>Direito linguístico do surdo de se comunicar com outros surdos.</strong> Direito de usar a língua de sinais para se integrar com os outros surdos, primeiro passo para uma integração na sociedade como um todo.</li>
                </ol>

