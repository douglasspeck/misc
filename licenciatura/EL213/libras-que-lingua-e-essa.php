                <h3>Todos os surdos fazem leitura labial?</h3>
                <p>Gostei da comparação com o turista perdido. De fato, é complicada a comunicação se os interlocutores, literalmente, não falam a mesma língua. Por isso é fundamental o aprendizado universal de Libras.</p>
