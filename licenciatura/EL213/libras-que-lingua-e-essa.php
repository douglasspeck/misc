            <section>
                <h3>O surdo tem uma identidade e uma cultura próprias?</h3>
                <p>Gostei do emprego do termo "sobrevivência cultural" para se referir ao uso de "culturas próprias". Isso é algo que observo também em outros grupos minoritários e que é importante para a identitarização dos mesmos.</p>
                <p>A cultura é flexível e, portanto, está sujeita à ação de fatores externos. Assim é com a cultura surda, que se adapta ao contexto em que se insere. Além disso, a identidade dessa cultura pode (e normalmente é) multidirecional, sem um sentido de unidade. Recentemente, a principal vertente da cultura surda reside em uma "contrarreação" ao grupo dominante.</p>
                <p>No caso da "cultura surda", é importante considerar as inúmeras facetas que diferenciam os surdos, seja por aspectos geográficos, sociais ou mesmo minoritários.</p>
