                <h3>O surdo precisa ser oralizado para se integrar na sociedade ouvinte?</h3>
                <p>A oralização é uma forma de violência estrutural que nega a natureza dos surdos e sua língua. Está associada a um sentimento de "correção" e possui vínculo histórico com movimentos eugenistas.</p>
                <p>Graham Bell era um grandissíssimo babaca.</p>
                <p>Por outro lado, o purismo linguístico do movimento 'anti-oralização' é também uma forma de disseminar preconceito, mesmo dentro da comunidade.</p>
