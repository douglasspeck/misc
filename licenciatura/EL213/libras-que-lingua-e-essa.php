                <p>Vários seres vivos podem comunicar-se, alguns de maneiras mais complexas que outros. Entretanto, <strong>somente os seres humanos possuem língua</strong>, de fato. Ademais, é importantíssimo destacar que <strong>a língua de sinais é língua</strong>.</p>
